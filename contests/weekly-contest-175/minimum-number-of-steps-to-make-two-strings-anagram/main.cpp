const ll INF = 1e18;

template <typename T>
inline bool chmax(T& a, T b) {
  if (a < b) {
    a = b;
    return 1;
  }
  return 0;
}

template <typename T>
inline bool chmin(T& a, T b) {
  if (a > b) {
    a = b;
    return 1;
  }
  return 0;
}

class Solution {
 public:
  int minSteps(string s, string t) {
    map<char, ll> s_ct_map;
    map<char, ll> t_ct_map;

    for (auto c : s) s_ct_map[c]++;
    for (auto c : t) t_ct_map[c]++;

    int res = 0;

    for (auto s_ct_pair : s_ct_map) {
      char c = s_ct_pair.fi;
      res += max(0ll, s_ct_map[c] - t_ct_map[c]);
    }

    return res;
  }
};

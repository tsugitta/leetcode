template <typename T>
inline bool chmax(T& a, T b) {
  if (a < b) {
    a = b;
    return 1;
  }
  return 0;
}

template <typename T>
inline bool chmin(T& a, T b) {
  if (a > b) {
    a = b;
    return 1;
  }
  return 0;
}

class Solution {
 public:
  bool checkIfExist(vector<int>& arr) {
    unordered_set<int> vals;

    for (auto e : arr) {
      if (vals.count(2 * e)) return true;
      if (e % 2 == 0 && vals.count(e / 2)) return true;
      vals.insert(e);
    }

    return false;
  }
};

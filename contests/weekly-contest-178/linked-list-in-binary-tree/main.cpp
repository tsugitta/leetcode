struct ListNode {
  int val;
  ListNode* next;
  ListNode(int x) : val(x), next(NULL) {}
};

struct TreeNode {
  int val;
  TreeNode* left;
  TreeNode* right;
  TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

class Solution {
 public:
  bool isSubPathStrictly(ListNode* head, TreeNode* root) {
    if (!head) return true;
    if (!root) return false;
    return head->val == root->val &&
           (isSubPathStrictly(head->next, root->left) ||
            isSubPathStrictly(head->next, root->right));
  }

  bool isSubPath(ListNode* head, TreeNode* root) {
    if (!root) return false;
    return isSubPathStrictly(head, root) || isSubPath(head, root->left) ||
           isSubPath(head, root->right);
  }
};

const ll INF = 1e18;

class Solution {
 public:
  int numSteps(string s) {
    int res = 0;
    int carry = 0;
    int len = s.size();

    for (auto i = len - 1; i > 0; --i) {
      ++res;

      if ((s[i] - '0') + carry == 1) {
        carry = 1;
        ++res;
      }
    }
    return res + carry;
  }
};

template <typename T>
inline bool chmax(T& a, T b) {
  if (a < b) {
    a = b;
    return 1;
  }
  return 0;
}

template <typename T>
inline bool chmin(T& a, T b) {
  if (a > b) {
    a = b;
    return 1;
  }
  return 0;
}

const ll INF = 1e18;

class Solution {
 public:
  int findTheDistanceValue(vector<int>& arr1, vector<int>& arr2, int d) {
    ll res = 0;

    for (auto v1 : arr1) {
      if (all_of(all(arr2), [&](ll v2) { return abs(v2 - v1) > d; })) {
        ++res;
      }
    }

    return res;
  }
};

template <typename T>
inline bool chmax(T& a, T b) {
  if (a < b) {
    a = b;
    return 1;
  }
  return 0;
}

template <typename T>
inline bool chmin(T& a, T b) {
  if (a > b) {
    a = b;
    return 1;
  }
  return 0;
}
